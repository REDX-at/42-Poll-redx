#include <unistd.h>

int	main(int ac, char **av)
{
	int	i = 0;
	int	s = 0;
	int	k = 0;

	if(ac == 2)
	{
		while(av[1][i] != '\0')
		{
			if(av[1][i] >= 'a' && av[1][i] <= 'z')
			{
				s = av[1][i] - 'a' + 1;
				k = 0;
				while(k < s)
				{
					write(1, &av[1][i], 1);
					k++;
				}
			}
			else if(av[1][i] >= 'A' && av[1][i] <= 'Z')
			{
				s = av[1][i] - 'A' + 1;
				k = 0;
				while(k < s)
				{
					write(1, &av[1][i], 1);
					k++;
				}
			}
			else 
			{
				write(1, &av[1][i], 1);
			}
			i++;
		}
	}
	write(1, "\n", 1);

}

#include <stdio.h>
#include <unistd.h>

int	main(int ac, char **av)
{
	int	count = 0;
	int	i = 0;

	if(ac == 2)
	{
		while(av[1][i] != '\0')
		{

			while(av[1][i] == 32 || av[1][i] == '\t')
			{
				i++;
			}
			while(av[1][i] != 32 && av[1][i] != '\t')
			{
					i++;
			}
			if(av[1][i] == 32 || av[1][i] == '\t' && av[1][i] != '\0')
			{
				count ++;
			}
			i++;
			
		}
	}
	write(1, "\n", 1);
	printf("%d", count);
}

 
